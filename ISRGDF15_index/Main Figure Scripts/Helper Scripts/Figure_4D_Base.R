# Based on: Scipts_for_Figures/gtex/Check these proliferation scripts/
#           P2025_02_14_ProliferationScores_per_PlaceofDeath.R
#
# Prerequisites:
#   - Run Scripts/gtex/Tissue_proliferation_index.R to generate proliferation data
#   - Hedges G results from deathplace analysis
#
# Output:
#   - Figure_4D.png: ISR scores by proliferative group
# ============================================================================

